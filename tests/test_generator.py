    def test_generate_complete_code(self):
        """Test complete code generation."""
        tasks = [
            {
                'expression': 'x**2',
                'variables': ['x'],
                'goal_type': 'evaluate',
                'parameters': {},
                'suggested_sampling': {
                    'method': 'uniform',
                    'points': 50,
                    'ranges': {'x': (-5, 5)}
                },
                'expected_output_type': 'function'
            }
        ]

        code = self.generator.generate_evaluation_code(tasks)

        self.assertIn('"""Generated by ParSub', code)
        self.assertIn("import numpy as np", code)
        self.assertIn("def lambdify_expr", code)
        self.assertIn("Task 1: Evaluate expression", code)
        self.assertIn('if __name__ == "__main__":', code)

    def test_save_code(self):
        """Test saving code to file."""
        tasks = [
            {
                'expression': 'x**2',
                'variables': ['x'],
                'goal_type': 'evaluate',
                'parameters': {},
                'suggested_sampling': {
                    'method': 'uniform',
                    'points': 50,
                    'ranges': {'x': (-5, 5)}
                },
                'expected_output_type': 'function'
            }
        ]

        code = self.generator.generate_evaluation_code(tasks)
        filepath = self.generator.save_code(code, "test_code.py")

        self.assertTrue(os.path.exists(filepath))
        self.assertTrue(filepath.endswith("test_code.py"))

        # Check that file contains expected content
        with open(filepath, 'r') as f:
            content = f.read()
        self.assertIn("Generated by ParSub", content)
        self.assertIn("x**2", content)


