"""
Unit tests for code generator.
"""

import unittest
import sys
import os
from pathlib import Path

# Add src to path
sys.path.insert(0, str(Path(__file__).parent.parent.parent / "src"))

from parsub.generator.code_generator import CodeGenerator


class TestCodeGenerator(unittest.TestCase):
    """Test cases for code generator."""

    def setUp(self):
        self.test_dir = "./test_output"
        self.generator = CodeGenerator(output_dir=self.test_dir)

    def tearDown(self):
        # Clean up test directory
        import shutil
        if os.path.exists(self.test_dir):
            shutil.rmtree(self.test_dir)

    def test_generator_initialization(self):
        """Test that generator initializes correctly."""
        self.assertIsInstance(self.generator, CodeGenerator)
        self.assertTrue(os.path.exists(self.test_dir))
        self.assertTrue(os.path.exists(os.path.join(self.test_dir, "plots")))
        self.assertTrue(os.path.exists(os.path.join(self.test_dir, "data")))

    def test_generate_header(self):
        """Test header generation."""
        header = self.generator._generate_header()
        self.assertIn("Generated by ParSub", header)
        self.assertIn('"""', header)

    def test_generate_imports(self):
        """Test imports generation."""
        imports = self.generator._generate_imports()
        self.assertIn("import numpy as np", imports)
        self.assertIn("import sympy as sp", imports)
        self.assertIn("import matplotlib.pyplot as plt", imports)

    def test_generate_helper_functions(self):
        """Test helper functions generation."""
        helpers = self.generator._generate_helper_functions()
        self.assertIn("def lambdify_expr", helpers)
        self.assertIn("def save_plot", helpers)
        self.assertIn("def save_data", helpers)

    def test_generate_evaluation_code_block(self):
        """Test evaluation code block generation."""
        task = {
            'expression': 'x**2',
            'variables': ['x'],
            'goal_type': 'evaluate',
            'parameters': {},
            'suggested_sampling': {
                'method': 'uniform',
                'points': 100,
                'ranges': {'x': (-5, 5)}
            },
            'expected_output_type': 'function'
        }

        code = self.generator._generate_evaluation_code_block(
            task['expression'],
            task['variables'],
            task['suggested_sampling'],
            1
        )

        self.assertIn("Task 1: Evaluate expression", code)
        self.assertIn("lambdify_expr", code)
        self.assertIn("np.linspace", code)
        self.assertIn("save_data", code)

